package academy.everyonecodes.java.assignment.exercise3;

public class Racer {
    private int speed;
    private int damage;

    public Racer(int speed, int damage) {
        this.speed = speed;
        this.damage = damage;
    }

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }
}
